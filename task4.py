"""
Завдання 4: Виведи число 9876.54321 з чотирма десятковими знаками після коми, 
округлене до найближчого значення, у форматі: "Округлене число: 9876.5432"
"""

# Задаємо число
number = 9876.54321

# Форматуємо з чотирма десятковими знаками (.4f)
formatted_string = f"Округлене число: {number:.4f}"

# Виводимо результат
print(formatted_string)