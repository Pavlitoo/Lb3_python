"""
Завдання 5: Створіть програму, яка запитує у користувача три різних числа (num1, num2, num3). 
Виведіть ці числа у форматованому вигляді з комами, щоб відокремити тисячі. 
Наприклад, якщо число дорівнює 1000000, то виведіть його як 1,000,000.
"""

# Запитуємо у користувача три числа
print("Введіть три числа:")
num1 = int(input("Перше число: "))
num2 = int(input("Друге число: "))
num3 = int(input("Третє число: "))

# Форматуємо числа з комами для відокремлення тисяч (:,)
print(f"Перше число з комами: {num1:,}")
print(f"Друге число з комами: {num2:,}")
print(f"Третє число з комами: {num3:,}")

# Альтернативний варіант - всі числа в одному рядку
print(f"Всі числа: {num1:,}, {num2:,}, {num3:,}")