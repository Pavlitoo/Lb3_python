"""
Завдання 8: Напишіть програму, яка використовує багаторядковий f-string для 
форматування інформації про книгу. Програма повинна запитувати у користувача:
1. Назву книги.
2. Ім'я американського автора.
3. Рік видання.
4. Кількість екземплярів.
5. Вартість книги (форматування з грошовим символом і двома знаками після коми).
6. Рейтинг книги (число з двома знаками після коми).

Всі числові значення повинні бути відформатовані:
- кількість екземплярів — цілочисельне значення з комами тисяч,
- вартість — з грошовим символом та двома знаками після коми,
- рейтинг — з двома знаками після коми.
"""

# Запитуємо інформацію у користувача
print("Введіть інформацію про книгу:")
book_title = input("Введіть назву книги: ")
author_name = input("Введіть автора (американського автора): ")
publication_year = int(input("Введіть рік видання: "))
copies_count = int(input("Введіть кількість екземплярів: "))
book_price = float(input("Введіть вартість книги: "))
book_rating = float(input("Введіть рейтинг книги (0-10): "))

# Створюємо багаторядковий f-string з інформацією про книгу
book_info = f"""
Інформація про книгу:
Назва: {book_title}
Автор: {author_name}
Рік видання: {publication_year}
Кількість екземплярів: {copies_count:,}
Вартість: ${book_price:.2f}
Рейтинг: {book_rating:.2f}
"""

# Виводимо інформацію
print(book_info)