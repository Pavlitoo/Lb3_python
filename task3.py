"""
Завдання 3: Виведи число 1234.56789 з двома десятковими знаками після коми у форматі: 
"Число з двома десятковими знаками: 1234.57"
"""

# Задаємо число
number = 1234.56789

# Форматуємо з двома десятковими знаками (.2f)
formatted_string = f"Число з двома десятковими знаками: {number:.2f}"

# Виводимо результат
print(formatted_string)