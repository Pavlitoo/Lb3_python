"""
Завдання 6: Виведи число 0.254 як відсоток з двома десятковими знаками після коми у 
форматі: "Відсоткове значення: 25.40%"
"""

# Задаємо число
ratio = 0.254

# Форматуємо як відсоток з двома десятковими знаками (.2%)
formatted_string = f"Відсоткове значення: {ratio:.2%}"

# Виводимо результат
print(formatted_string)